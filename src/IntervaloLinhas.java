
public class IntervaloLinhas {
	int inicio;
	int fim;

	public IntervaloLinhas() {

	}

	public IntervaloLinhas(int inicio, int fim) {

		this.inicio = inicio;
		this.fim = fim;
	}

	public int getInicio() {
		return inicio;
	}

	public void setInicio(int inicio) {
		this.inicio = inicio;
	}

	public int getFim() {
		return fim;
	}

	public void setFim(int fim) {
		this.fim = fim;
	}

}
